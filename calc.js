class calc {
    add(numOne, numTwo) {
        return numOne + numTwo;
    };
    subtract(numOne, numTwo) {
        return numOne - numTwo;
    };
    divide(numOne, numTwo) {
        return numOne / numTwo;
    };
    multiply(numOne, numTwo) {
        return numOne * numTwo;
    };
}

module.exports = calc;